func chooseTranslationDB() -> [Message] {
    var db: [Message] = [.init(id: "", contents: [""])]
    
    // Based on language assign right array of translations
    if let lang = Bundle.main.preferredLocalizations.first { // "en", "fr", "ru"
        switch lang {
        case "en":
            db = database1
        case "fr":
            db = database2
        default: // ru
            db = database3
        }
    }
    return db
}

